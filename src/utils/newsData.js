// 获取所有新闻
export const getAllNews = () => {
  return [
    ...newsData.companyNews,
    ...newsData.industryNews,
    ...newsData.productUpdates
  ].sort((a, b) => new Date(b.publishDate) - new Date(a.publishDate))
}

// 根据分类获取新闻
export const getNewsByCategory = (category) => {
  const allNews = getAllNews()
  
  switch (category) {
    case 'all':
      return allNews
    case 'company':
      return newsData.companyNews
    case 'industry':
      return newsData.industryNews
    case 'product':
      return newsData.productUpdates
    case 'hot':
      return allNews.filter(news => news.isHot)
    case 'new':
      return allNews.filter(news => news.isNew)
    default:
      return allNews
  }
}

// 根据ID获取新闻详情
export const getNewsById = (id) => {
  const allNews = getAllNews()
  return allNews.find(news => news.id === parseInt(id))
}

// 搜索新闻
export const searchNews = (keyword) => {
  const allNews = getAllNews()
  return allNews.filter(news => 
    news.title.includes(keyword) || 
    news.summary.includes(keyword) ||
    news.content.includes(keyword) ||
    news.tags.some(tag => tag.includes(keyword))
  )
}
